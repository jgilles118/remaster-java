/*
 * A simple array list and using a for loop to display the list.
 *
 *
 * */

class array1{

    public static void main(String[] args){

        String[] cars = {"Mercedes Benz","Jaguar","Aston Martin","Bugatti","Cadilac"};

        for(int brand = 0; brand < cars.length; brand++){

            System.out.println(cars[brand]);

        }//close for loop

    }//close main


}//close array1
